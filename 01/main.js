var mapWidth = map[0].length
var mapHeight = map.length
// Размер в зависимости от высоты окна
var miniMapScale = 16

function init() {
     mapWidth = map[0].length
     mapHeight = map.length

    drawMiniMap()
}

function drawMiniMap() {
    // Получение элемента
    let miniMap = $("display")
    // Установка размеров холста
    miniMap.width = mapWidth * miniMapScale
    miniMap.height = mapHeight * miniMapScale

    miniMap.style.width = (mapWidth * miniMapScale) + "px"
    miniMap.style.height = (mapHeight * miniMapScale) + "px"
    // Получение контекста рисования
    let ctx = miniMap.getContext("2d")

    // Каждый блок нарисовать на холсте
    for (let y = 0; y < mapHeight; y++) {
        for (let x = 0; x < mapWidth; x++) {

            let wall = map[y][x];
            // Если в ячейке значение положительно...
            if (wall > 0) {

                ctx.fillStyle = "rgb(170,170,170)";
                ctx.fillRect( // ... нарисовать блок на холсте
                    x * miniMapScale, // X
                    y * miniMapScale, // Y
                    miniMapScale, miniMapScale // Width, Height
                );

            }
        }
    }
}

window.addEventListener("load", () => init())
